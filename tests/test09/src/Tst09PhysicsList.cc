void Tst09PhysicsList::SetCuts()
{
  if (verboseLevel >0){
    G4cout << "Tst09PhysicsList::SetCuts:";
    G4cout << "Default CutLength : " << defaultCutValue/mm << " (mm)" 
	   << G4endl;
  }  

  //  " G4VUserPhysicsList::SetCutsWithDefault" method sets 
  //   the default cut value for all particle types 
  SetCutsWithDefault();   
}

