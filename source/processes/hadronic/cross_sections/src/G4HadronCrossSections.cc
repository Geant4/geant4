// Method to calculate cross sections for all processes.
// To facilitate comparison with the original Fortran source, and because
// of the interdependence of the elastic and inelastic cross section
// calculations, this has not been split into separate processes.

void
G4HadronCrossSections::CalcScatteringCrossSections(
                          const G4DynamicParticle* aParticle,
                          const G4Element* anElement)
{
   G4double sigel, sigin, sigtot;
   G4double xsecel, xsecin, csaa;
   G4double dx, dy, rc, rca, b;
   G4double crel, crin;
   G4double xspiel, xspiin;

   G4int ipart = GetParticleCode(aParticle);
   G4double a = anElement->GetA()*mole/g;
   G4double z = anElement->GetZ();

   if (verboseLevel > 1) {
      G4cout << "G4HadronCrossSections: a=" << a << G4endl;
      G4cout << "G4HadronCrossSections: z=" << z << G4endl;
   }

// Ions...

   if (ipart >= 30 && ipart <= 32) {

      G4double apart;
      if (ipart == 30) apart = pow(2., 1./3.);
      else if (ipart == 31) apart = pow(3., 1./3.);
      else if (ipart == 32) apart = pow(4., 1./3.);

      G4double term = apart + pow(a, 1./3.);
      sigin = 49.*term*term;
   // Convert cross section from mb to default units
      siginelastic = sigin*millibarn;
      if(aParticle->GetKineticEnergy() < 6*MeV) siginelastic = 0;
      sigelastic = 0.;
      return;
   }

   G4double ek = aParticle->GetKineticEnergy()/GeV;

// Low energy neutrons...

   if (ipart == 16 && ek <= 0.0327) {

     //      G4int je2 = NELAB;
     //      for (G4int j = 2; j <= NELAB; j++) {
     //         if (ek < elab[j - 1]) {
     //            je2 = j;
     //            break;
     //         }
     //      }
     //      G4int je1 = je2 -1;
     //      je1 = je1 - 1;      // For array indexing
     //      je2 = je2 - 1;      // For array indexing

      G4int je1 = 0;
      G4int je2 = NELAB - 1;
      do {
         G4int midBin = (je1 + je2)/2;
         if (ek < elab[midBin])
           je2 = midBin;
         else
           je1 = midBin;
      } while (je2 - je1 > 1); 

      G4double ekx = G4std::max(ek, 1.e-9);
      G4double delab = elab[je2] - elab[je1];

      //      G4int ja2 = NCNLW;
      //      for (G4int jj = 2; jj <= NCNLW; jj++) {
      //         if (a < cnlwat[jj - 1]) {
      //            ja2 = jj;
      //            break;
      //         }
      //      }
      //      G4int ja1 = ja2 - 1;
      //      ja1 = ja1 - 1;      // For array indexing
      //      ja2 = ja2 - 1;      // For array indexing

      G4int ja1 = 0;
      G4int ja2 = NCNLW - 1;
      do {
         G4int midBin = (ja1 + ja2)/2;
         if (a < cnlwat[midBin])
           ja2 = midBin;
         else
           ja1 = midBin;
      } while (ja2 - ja1 > 1); 

      G4double dnlwat = cnlwat[ja2] - cnlwat[ja1];

// Elastic cross section:
      // E interpolation or extrapolation at JA1
      dy = cnlwel[ja1][je2] - cnlwel[ja1][je1];
      G4double rce = dy/delab;
      // A interpolation or extrapolation at JE1
      dy = cnlwel[ja2][je1] - cnlwel[ja1][je1];
      rca = dy/dnlwat;
      b = cnlwel[ja1][je1] - rce*elab[je1] - rca*cnlwat[ja1];
      sigelastic = rce*ek + rca*a + b;
// Inelastic cross section:
      // E interpolation or extrapolation at JA1
      dy = cnlwin[ja1][je2] - cnlwin[ja1][je1];
      rce = dy/delab;
      // A interpolation or extrapolation at JE1
      dy = cnlwin[ja2][je1] - cnlwin[ja1][je1];
      rca = dy/dnlwat;
      b = cnlwin[ja1][je1] - rce*elab[je1] - rca*cnlwat[ja1];
      siginelastic = rce*ek + rca*a + b;
   // Convert cross sections from mb to default units
      sigelastic = sigelastic*millibarn;
      siginelastic = siginelastic*millibarn;
      return;
   }

// Remaining particles...

// Get momentum bin
   G4double p = aParticle->GetTotalMomentum()/GeV;

   //   G4int j = TSIZE - 1;
   //   for (G4int i = 2; i <= TSIZE; i++) {
   //      if (p < plab[i]) {
   //         j = i - 1;
   //         break;
   //      }
   //   }
   //   j = j - 1;                     // For array indexing

   G4int je1 = 0;
   G4int je2 = TSIZE - 1;
   do {
      G4int midBin = (je1 + je2)/2;
      if (p < plab[midBin])
        je2 = midBin;
      else
        je1 = midBin;
   } while (je2 - je1 > 1); 

   G4int ipart1 = ipart - 1;      // For array indexing

// Get cross sections for scattering on free protons
   dx = plab[je2] - plab[je1];
// Elastic cross section
   dy = csel[ipart1][je2] - csel[ipart1][je1];
   rc = dy/dx;
   b = csel[ipart1][je1] - rc*plab[je1];
   sigel = rc*p + b;
// Inelastic cross section
   dy = csin[ipart1][je2] - csin[ipart1][je1];
   rc = dy/dx;
   b = csin[ipart1][je1] - rc*plab[je1];
   sigin = rc*p + b;
   if (verboseLevel > 1) {
      G4cout << "sigel " << sigel << G4endl;
      G4cout << "sigin " << sigin << G4endl;
   }
   G4double alph = alpha[ipart1];
   if (ipart < 14) {
      dy = alphac[je2] - alphac[je1];
      rc = dy/dx;
      b = alphac[je1] - rc*plab[je1];
      G4double corfac = rc*p + b;
      alph = alph*corfac;
      G4int ipart3 = ipart2[ipart - 7];
      ipart3 = ipart3 - 1;      // For array indexing
      // Elastic cross section
      dy = csel[ipart3][je2] - csel[ipart3][je1];
      rc = dy/dx;
      b = csel[ipart3][je1] - rc*plab[je1];
      xsecel = rc*p + b;
      // Inelastic cross section
      dy = csin[ipart3][je2] - csin[ipart3][je1];
      rc = dy/dx;
      b = csin[ipart3][je1] - rc*plab[je1];
      xsecin = rc*p + b;
   }

// A-dependence from parameterization...

   if (a >= 1.5) {

      crel = 1.;
      crin = 1.;

      G4int i = 3;
      if (a < 50.) i = 2;
      if (a > 100.) i = 4;
      i = i - 1;      // For array indexing

// Protons and neutrons
      if (ipart == 14 || ipart == 16) {
         dy = cspnel[i - 1][je2] - cspnel[i - 1][je1];
         rc = dy/dx;
         b = cspnel[i - 1][je1] - rc*plab[je1];
         xsecel = rc*p + b;
         dy = cspnin[i - 1][je2] - cspnin[i - 1][je1];
         rc = dy/dx;
         b = cspnin[i - 1][je1] - rc*plab[je1];
         xsecin = rc*p + b;
         // The following is a first-order correction to Gheisha (GHESIG)
         // behaviour, where for the lighter elements the inelastic cross
         // section is not realistic for particles in the first momentum
         // bin.  In the first momentum bin, it better to interpolate 
         // in K.E.  Subject to further improvements.
         if (correctInelasticNearZero && je1 == 0 && i <= 3) {
            G4double m0 = aParticle->GetMass()/GeV;
            G4double T = sqrt(m0*m0 + p*p) - m0;
            G4double dx = sqrt(m0*m0 + plab[1]*plab[1]) - m0;
            rc = dy/dx;
            xsecin = rc*T + b;
         }

         if (sigel >= 0.001) 
            crel = xsecel/(0.36*sigel*pow(G4double(csa[i]), 1.17));
         sigtot = sigel + sigin;
         if (sigtot >= 0.001) 
            crin = xsecin/(sigtot*pow(G4double(csa[i]), alph));
      }

      else if (ipart < 15) {
// Calculate correction factors (crel, crin) from values
// on Al, Cu, Pb.  Note that data is only available for pions and protons.
         G4double wgch = 0.5;
         if (a < 20.) wgch = 0.5 + 0.5*exp(-(a - 1.));
         sigel = wgch*sigel + (1. - wgch)*xsecel;
         sigin = wgch*sigin + (1. - wgch)*xsecin;
         
// This section not for kaons
         if (ipart < 10) {
            dy = cspiel[i - 1][je2] - cspiel[i - 1][je1];
            rc = dy/dx;
            b = cspiel[i - 1][je1] - rc*plab[je1];
            xspiel = rc*p + b;
            dy = cspiin[i - 1][je2] - cspiin[i - 1][je1];
            rc = dy/dx;
            b = cspiin[i - 1][je1] - rc*plab[je1];
            xspiin = rc*p + b;
            if (verboseLevel > 1) {
               G4cout << "xspiel " << xspiel << G4endl;
               G4cout << "xspiin " << xspiin << G4endl;
            }               
            if (sigel >= 0.001) 
               crel = xspiel/(0.36*sigel*pow(G4double(csa[i]),1.17));
            sigtot = sigel + sigin;
            if (sigtot >= 0.001) 
               crin = xspiin/(sigtot*pow(G4double(csa[i]), alph));
         }
      }

// Apply correction factors
      sigin = crin*(sigin + sigel)*pow(a, alph);
      sigel = crel*0.36*sigel*pow(a, 1.17);
      sigel = sigel*partel[ipart1];
      sigin = sigin*partin[ipart1];
   }

// Correction factor for high (p > 100 GeV/c) energies:
   G4double corh = 1.;
   if (p > 100.) corh = 0.1085736156*log(p) + 0.5;

   sigel = corh*sigel;
   sigin = corh*sigin;

   // Convert cross section from mb to default units
   sigelastic = sigel*millibarn;
   siginelastic = sigin*millibarn;

   return;
}
   

G4double
G4HadronCrossSections::GetCaptureCrossSection(
                          const G4DynamicParticle* aParticle,
                          const G4Element* anElement)
{
   if (GetParticleCode(aParticle) != 16) return 0.;
   G4double ek = aParticle->GetKineticEnergy()/GeV;
   if (ek > 0.0327) return 0.;

   G4double ekx = G4std::max(ek, 1.e-9);
   G4double z = anElement->GetZ();
   G4int izno = z + 0.01;
   if (izno > 100) izno = 100;      // Not in GHESIG
   izno = izno - 1;      // For array indexing
   G4double sigcap = 11.12*cscap[izno]/pow(ekx*1.e6, 0.577);
   // Convert cross section from mb to default units
   sigcap = sigcap*millibarn;
   return sigcap;
}


G4double
G4HadronCrossSections::GetFissionCrossSection(
                          const G4DynamicParticle* aParticle,
                          const G4Element* anElement)
{
   G4double a = anElement->GetA()*mole/g;
   if (a < 230.) return 0;

   G4double z = anElement->GetZ();

   G4double ek = aParticle->GetKineticEnergy()/GeV;      

   //   G4int i = NFISS;
   //   for (G4int ii = 1; i <= NFISS; i++) {
   //      if (ek < ekfiss[ii - 1]) {
   //         i = ii;
   //         break;
   //      }
   //   }
   //   i = i - 1;      // For array indexing

   G4int ie1 = 0;
   G4int ie2 = NFISS - 1;
   do {
      G4int midBin = (ie1 + ie2)/2;
      if (ek < ekfiss[midBin])
        ie2 = midBin;
      else
        ie1 = midBin;
   } while (ie2 - ie1 > 1); 
   G4int i = ie2;
   if (ek < ekfiss[0]) i = 0;

   G4int j = 4;
   if (ek <= 0.01) {
      if (z == 92. && abs(a - 233.) < 0.5) j = 1;
      else if (z == 92. && abs(a - 235.) < 0.5) j = 2;
      else if (z == 94. && abs(a - 239.) < 0.5) j = 3;
   }
   G4double z43ba;
   if (j == 4) {
      z43ba = pow(z, 4./3.)/a;
      z43ba = G4std::max(-67. + 38.7*z43ba, 0.);
   }
   else {
      z43ba = 1.;
   }
   j = j - 1;      // For array indexing
   G4double sigfiss = csfiss[j][i]*z43ba;
// Convert from mb to cm^2
   //         sigfiss = sigfiss*1.e-27;
   // Convert cross section from mb to default units
   sigfiss = sigfiss*millibarn;
   return sigfiss;
}


G4int
G4HadronCrossSections::GetParticleCode(const G4DynamicParticle* aParticle)
{
// Returns GHEISHA code for particle...
// There are 35 particle codes of which only the hadrons are treated here.

   G4int ipart;
   G4ParticleDefinition* aParticleType = aParticle->GetDefinition();

   if (aParticleType == G4PionPlus::PionPlus()) ipart = 7;
   else if (aParticleType == G4PionZero::PionZero()) ipart = 8;
   else if (aParticleType == G4PionMinus::PionMinus()) ipart = 9;
   else if (aParticleType == G4KaonPlus::KaonPlus()) ipart = 10;
   else if (aParticleType == G4KaonZeroShort::KaonZeroShort()) ipart = 11;
   else if (aParticleType == G4KaonZeroLong::KaonZeroLong()) ipart = 12;
   else if (aParticleType == G4KaonMinus::KaonMinus()) ipart = 13;
   else if (aParticleType == G4Proton::Proton()) ipart = 14;
   else if (aParticleType == G4AntiProton::AntiProton()) ipart = 15;
   else if (aParticleType == G4Neutron::Neutron()) ipart = 16;
   else if (aParticleType == G4AntiNeutron::AntiNeutron()) ipart = 17;
   else if (aParticleType == G4Lambda::Lambda()) ipart = 18;
   else if (aParticleType == G4AntiLambda::AntiLambda()) ipart = 19;
   else if (aParticleType == G4SigmaPlus::SigmaPlus()) ipart = 20;
   else if (aParticleType == G4SigmaZero::SigmaZero()) ipart = 21;
   else if (aParticleType == G4SigmaMinus::SigmaMinus()) ipart = 22;
   else if (aParticleType == G4AntiSigmaPlus::AntiSigmaPlus()) ipart = 23;
   else if (aParticleType == G4AntiSigmaZero::AntiSigmaZero()) ipart = 24;
   else if (aParticleType == G4AntiSigmaMinus::AntiSigmaMinus()) ipart = 25;
   else if (aParticleType == G4XiZero::XiZero()) ipart = 26;
   else if (aParticleType == G4XiMinus::XiMinus()) ipart = 27;
   else if (aParticleType == G4AntiXiZero::AntiXiZero()) ipart = 28;
   else if (aParticleType == G4AntiXiMinus::AntiXiMinus()) ipart = 29;
   else if (aParticleType == G4Deuteron::Deuteron()) ipart = 30;
   else if (aParticleType == G4Triton::Triton()) ipart = 31;
   else if (aParticleType == G4Alpha::Alpha()) ipart = 32;
   else if (aParticleType == G4OmegaMinus::OmegaMinus()) ipart = 33;
   else if (aParticleType == G4AntiOmegaMinus::AntiOmegaMinus()) ipart = 34;
   else {
      G4Exception("G4HadronCrossSections: unsupported particle");
      return 0;
   }
   return ipart;
}
