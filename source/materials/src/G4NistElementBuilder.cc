G4int G4NistElementBuilder::GetZ(const G4String& name) const
{
  G4int Z = maxNumElements;
  // Loop checking, 07-Aug-2015, Vladimir Ivanchenko
  do {--Z;} while( Z>0 && elmSymbol[Z] != name);
  return Z;
}

