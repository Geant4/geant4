G4Element* G4NistElementBuilder::FindOrBuildElement(G4int Z, G4bool)
{
  G4Element* anElement = FindElement(Z);
  if(anElement == nullptr && Z > 0 && Z < maxNumElements) {
    anElement = BuildElement(Z);
  }
  return anElement;
}

