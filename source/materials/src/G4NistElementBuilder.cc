G4NistElementBuilder::G4NistElementBuilder(G4int vb):
  verbose(vb)
{
  nFirstIsotope[0] = 0;
  nIsotopes[0] = 0;
  relAbundance[0] = 0.0;
  Initialise();
  for(int& i : elmIndex)
  {
    i = -1;
  }
}

